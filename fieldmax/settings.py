def validate_settings():
    """Validate critical settings on startup"""
    import sys
    
    errors = []
    
    # Check if logs directory exists
    if not LOGS_DIR.exists():
        try:
            LOGS_DIR.mkdir(parents=True)
        except Exception as e:
            errors.append(f"Cannot create logs directory: {e}")
    
    # Check if media directory exists
    if not MEDIA_ROOT.exists():
        try:
            MEDIA_ROOT.mkdir(parents=True)
        except Exception as e:
            errors.append(f"Cannot create media directory: {e}")
    
    # Warn if DEBUG is True in production
    if not DEBUG and SECRET_KEY.startswith('django-insecure-'):
        errors.append("WARNING: Using insecure SECRET_KEY in production!")
    
    if errors:
        print("\n‚ö†Ô∏è  SETTINGS VALIDATION ERRORS:")
        for error in errors:
            print(f"   - {error}")
        print()

# Run validation
